function SignOut() {
  return auth.currentUser && (
    <button className="flex justify-center items-center gap-2 p-3 bg-red-400 text-black rounded-lg hover:scale-125 transition ease-out"
    onClick={() => auth.signOut()}>Logout</button>
  )
}


function ChatRoom() {
  const dummy = useRef();
  const messagesRef = firestore.collection('messages');
  const query = messagesRef.orderBy('createdAt').limit(25);
  //usseCollectionData allow data to be change in real-time react pu re render
  const [messages] = useCollectionData(query, { idField: 'id' });

  const [formValue, setFormValue] = useState('');


  const sendMessage = async (e) => {
    e.preventDefault();

    const { uid, photoURL } = auth.currentUser;

    await messagesRef.add({
      text: formValue,
      createdAt: firebase.firestore.FieldValue.serverTimestamp(),
      uid,
      photoURL
    })

    setFormValue('');
    dummy.current.scrollIntoView({ behavior: 'smooth' });
  }

  return (<>
    <main className="lg:border-2 rounded-lg lg:h-[50vh] max-h-[50vh] w-[100vw] lg:w-[80vw] overflow-scroll scrollbar-hide">

      {messages && messages.map(msg => <ChatMessage key={msg.id} message={msg} />)}

      <span ref={dummy}></span>

    </main>

    <form className="h-[10vh] rounded-lg mt-5 flex justify-evenly items-center " 
    onSubmit={sendMessage}>

      <input className="rounded-lg h-[10vh] w-[70%] bg-black border-2"
       value={formValue} onChange={(e) => setFormValue(e.target.value)} placeholder="Type something..." />

      <button className="cursor-pointer bg-blue-400 rounded-lg p-5 lg:px-20 lg:py-5" type="submit" disabled={!formValue}>Send</button>

    </form>
  </>)
}


